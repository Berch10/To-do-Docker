/**
 * Enum representing the sort order for operations.
 *
 * This enum is used to specify whether items should be sorted in ascending or descending order.
 *
 * @enum {string}
 */
export enum Order {
  /**
   * Specifies sorting in ascending order.
   */
  ASC = 'asc',

  /**
   * Specifies sorting in descending order.
   */
  DESC = 'desc',
}
